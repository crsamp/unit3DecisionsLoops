public class DoWhile
{
    public static void main(String[] args)
    {
        int i = 1;
        
        do{
            System.out.println("1 " + i);
            i++;
        } while (i < 5);
        
        i = 1;
        
        do{
            System.out.println("2 " + i);
            i++;
        } while (i < 5);
    }
}
